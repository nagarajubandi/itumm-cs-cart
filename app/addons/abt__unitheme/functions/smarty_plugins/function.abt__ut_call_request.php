function smarty_function_abt__ut_call_request($params, &$smarty){
$addons = $smarty->getTemplateVars('addons');
if (!empty($addons['call_requests']) and $addons['call_requests']['status'] == 'A'){
include_once(Registry::get('config.dir.addons') . 'call_requests/functions/smarty_plugins/function.call_request.php');
return smarty_function_call_request($params, $smarty);
}else return false;
}
